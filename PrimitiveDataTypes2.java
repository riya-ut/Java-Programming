public class PrimitiveDataTypes2 {
    public static void main(String[] args) {
        byte smallNumber = 127;
        short mediNumber = 32000;
        int number = 100000;
        long largeNumber = 123456789L;
        float decimalvalue = 10.5f;
        double largeDecimalValue = 12345.6789;
        char intial = 'v';
        boolean isJavaFun = true;
        System.out.println("Byte: " + smallNumber);
        System.out.println("Short:" + mediNumber);
        System.out.println("Inte: " + number);
        System.out.println("Long:" + largeNumber);
        System.out.println("Float" + decimalvalue);
        System.out.println("Double:" + largeDecimalValue);
        System.out.println("char: " + intial);
        System.out.println("Boolean" + isJavaFun);
    }
}
